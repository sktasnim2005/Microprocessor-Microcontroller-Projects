CODE SEGMENT
    ASSUME CS:CODE,DS:CODE,ES:CODE,SS:CODE

PPIC_C EQU 06H
PPIC   EQU 04H
PPIB   EQU 02H
PPIA   EQU 00H


    ORG 0000H
    MOV AL,10000000B
    OUT PPIC_C,AL
    MOV BL,11111110B

L1: MOV AL,11111111B
    OUT PPIA,AL
    MOV AL,BL
    OUT PPIB,AL

    CALL DELAY
    ROL BL,1

    JMP L1


DELAY: MOV CX,0FFFFH
TIMER1: NOP
    NOP
    NOP
    NOP
    LOOP TIMER1
    RET

CODE ENDS
END